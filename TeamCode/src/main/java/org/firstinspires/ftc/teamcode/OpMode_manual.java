package org.firstinspires.ftc.teamcode;


import com.arcrobotics.ftclib.command.MecanumControllerCommand;
import com.arcrobotics.ftclib.drivebase.MecanumDrive;
import com.arcrobotics.ftclib.hardware.motors.Motor;
import com.arcrobotics.ftclib.hardware.motors.SimpleMotor;
import com.qualcomm.robotcore.eventloop.opmode.OpMode;
import com.qualcomm.robotcore.eventloop.opmode.TeleOp;
import com.qualcomm.robotcore.hardware.DcMotor;
import com.qualcomm.robotcore.hardware.DcMotorSimple;
import com.qualcomm.robotcore.hardware.Servo;


@TeleOp(name = "OpMode_manual")
public class OpMode_manual extends OpMode {
    MecanumDrive car;
    DcMotor frontLeft, frontRight, backLeft, backRight;
    Servo sv;

    @Override
    public void init() {
        frontLeft = hardwareMap.dcMotor.get("frontLeft");
        frontRight = hardwareMap.dcMotor.get("frontRight");
        backLeft = hardwareMap.dcMotor.get("backLeft");
        backRight = hardwareMap.dcMotor.get("backRight");
        sv = hardwareMap.servo.get("sv");

        car = new MecanumDrive(
                new SimpleMotor("frontLeft", hardwareMap),
                new SimpleMotor("frontRight", hardwareMap),
                new SimpleMotor("backLeft", hardwareMap),
                new SimpleMotor("backRight", hardwareMap));


    }


    double turn = 0;

    @Override
    public void loop() {

        car.driveRobotCentric(gamepad1.left_stick_x,
                gamepad1.left_stick_y,
                gamepad1.right_trigger - gamepad1.left_trigger);

    }

    @Override
    public void stop() {
    }

}

